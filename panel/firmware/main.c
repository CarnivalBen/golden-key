void main(void) {
    
    TRISA = 0b11111111;
    TRISC = 0b00000000;    
    
    while (1) {
        
        PORTC = getHallSensorValue(0);
        
    }
    
    
}


byte getHallSensorValue(byte id) {
    
    byte channelSelect = id << 3;    
    
    ADCON1 = 0b01000100;
    ADCON0 = 0b10000101 | channelSelect;
    while (ADCON0bits.GO_nDONE);
    
    return ADRESH;
    
}